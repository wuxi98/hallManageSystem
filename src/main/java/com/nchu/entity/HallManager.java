package com.nchu.entity;

public class HallManager {
	private String hallManagerId;
	private String hallManagerName;
	private String hallManagerPassword;

	public String getHallManagerId() {
		return hallManagerId;
	}

	public void setHallManagerId(String hallManagerId) {
		this.hallManagerId = hallManagerId;
	}

	public String getHallManagerName() {
		return hallManagerName;
	}

	public void setHallManagerName(String hallManagerName) {
		this.hallManagerName = hallManagerName;
	}

	public String getHallManagerPassword() {
		return hallManagerPassword;
	}

	public void setHallManagerPassword(String hallManagerPassword) {
		this.hallManagerPassword = hallManagerPassword;
	}
}
